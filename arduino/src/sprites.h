// Sprite size
#define SPRITE_W 16
#define SPRITE_H 16

// Draw a sprite with scaling and transparency
inline void drawSprite(LovyanGFX* lcd, int x, int y, const uint16_t* sprite, int scale, uint16_t bgColor) {
    for (int row = 0; row < SPRITE_H; row++) {
        for (int col = 0; col < SPRITE_W; col++) {
            uint16_t pixel = pgm_read_word(&sprite[row * SPRITE_W + col]);
            if (pixel != SPRITE_TRANSPARENT) {
                lcd->fillRect(x + col * scale, y + row * scale, scale, scale, pixel);
            }
        }
    }
}

// Draw sprite to M5Canvas (for double buffering)
inline void drawSpriteToCanvas(M5Canvas* canvas, int x, int y, const uint16_t* sprite, int scale) {
    for (int row = 0; row < SPRITE_H; row++) {
        for (int col = 0; col < SPRITE_W; col++) {
            uint16_t pixel = pgm_read_word(&sprite[row * SPRITE_W + col]);
            if (pixel != SPRITE_TRANSPARENT) {
                canvas->fillRect(x + col * scale, y + row * scale, scale, scale, pixel);
            }
        }
    }
}

